# Користувач вводить текст
input_string = input("Введіть текст: ")

# Користувач вводить символ для пошуку по введеному тексті
search_char = input("Введіть символ для пошуку: ")

# Ініціалізуємо пошук символів
count = 0

# Через цикл перевіряємо кожен символ у тексті
for char in input_string:
    if char == search_char:
        count += 1

# Виводимо результат
print(f"Символ '{search_char}' зустрічається у тексті {count} разів.")